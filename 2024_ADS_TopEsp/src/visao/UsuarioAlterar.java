    private void btnAltSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltSenhaActionPerformed
        if (tfSenha != null && tfConfSenha != null) {
            if (tfSenha == tfConfSenha) {
                try {
                    Integer id = Integer.valueOf(tfID.getText());
                    String senha = tfSenha.getText();

                    Usuario u = new Usuario();
                    u.setId(id);
                    u.setSenha(senha);
                    
                    UsuarioDao dao = new UsuarioDao();
                    dao.atualizarSenha(u);
                    JOptionPane.showMessageDialog(this, "Senha atualizada com sucesso.");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "As senhas devem ser id√™nticas.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Campo em branco, favor preenche-los corretamente.");
        }
    }//GEN-LAST:event_btnAltSenhaActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Integer id = Integer.valueOf(tfID.getText());
        String nome = tfNome.getText();
        String email = tfEmail.getText();

        Usuario u = new Usuario();
        u.setId(id);
        u.setNome(nome);
        u.setEmail(email);

        try {
            UsuarioDao dao = new UsuarioDao();
            dao.atualizar(u);

            JOptionPane.showMessageDialog(this, "Registro atualizado com sucesso.");
            linkUsuarioGerenciarForm.atualizarTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

