    public void mostrarUsuario(Integer id) {
        try {
            UsuarioDao dao = new UsuarioDao();
            Usuario u = dao.getUsuario(id);
            if (u != null) {
                tfID.setText(id.toString());
                tfEmail.setText(u.getEmail());
                tfNome.setText(u.getNome());
            } else {
                JOptionPane.showMessageDialog(this, "Usu√°rio n√£o existe.");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }

